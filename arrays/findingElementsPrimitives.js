const numbers = [1, 2, 3, 4, 3, 3];

// indexOf retorna o index do valor desejado
console.log(numbers.indexOf(1));

// lastIndexOf retorna o index do último número apresentado no array
console.log(numbers.lastIndexOf(3));

// Verifica se existe o número apresentado no array, e retorna um valor booleano
console.log(numbers.includes(3));
